const booksContainer = document.querySelector('.book-cards'); // Змінено на .book-cards
const filterButtons = document.querySelectorAll('.filter-button');
const paginationContainer = document.getElementById('pagination-container'); // Додайте цей елемент у HTML

const booksPerPage = 9;
let currentPage = 1;
let currentFilter = 'all';

function displayBooks(filter, page) {
    let filteredBooks = [];
    if (filter === 'all') {
        filteredBooks = books;
    } else {
        filteredBooks = books.filter(book => book.genre === filter);
    }

    const startIndex = (page - 1) * booksPerPage;
    const endIndex = startIndex + booksPerPage;
    const booksToDisplay = filteredBooks.slice(startIndex, endIndex);

    booksContainer.innerHTML = '';
    booksToDisplay.forEach(book => {
        const bookCard = `
            <div class="book-card">
                <img src="${book.image}" alt="${book.title}">
                <div class="book-info">
                    <h3>${book.title}</h3>
                    <p>Автор: ${book.author}</p>
                    <p class="book-description">${book.description}</p>
                    <button class="toggle-description">Розгорнути</button>
                </div>
            </div>
        `;
        booksContainer.innerHTML += bookCard;
    });

    addToggleDescriptionListeners();
    setupPagination(filteredBooks.length);
}

function setupPagination(totalBooks) {
    const paginationContainer = document.getElementById('pagination-container');
    paginationContainer.innerHTML = '';
    const totalPages = Math.ceil(totalBooks / booksPerPage);

    if (totalPages <= 1) return;

    // Кнопка "Попередня"
    const prevButton = document.createElement('button');
    prevButton.innerHTML = '&larr;';
    prevButton.classList.add('page-button', 'arrow');
    prevButton.disabled = currentPage === 1;
    prevButton.addEventListener('click', () => {
        if (currentPage > 1) {
            currentPage--;
            displayBooks(currentFilter, currentPage);
        }
    });
    paginationContainer.appendChild(prevButton);

    // Нумеровані кнопки
    for (let i = 1; i <= totalPages; i++) {
        const pageButton = document.createElement('button');
        pageButton.textContent = i;
        pageButton.classList.add('page-button');
        if (i === currentPage) pageButton.classList.add('active');
        pageButton.addEventListener('click', () => {
            currentPage = i;
            displayBooks(currentFilter, currentPage);
        });
        paginationContainer.appendChild(pageButton);
    }

    // Кнопка "Наступна"
    const nextButton = document.createElement('button');
    nextButton.innerHTML = '&rarr;';
    nextButton.classList.add('page-button', 'arrow');
    nextButton.disabled = currentPage === totalPages;
    nextButton.addEventListener('click', () => {
        if (currentPage < totalPages) {
            currentPage++;
            displayBooks(currentFilter, currentPage);
        }
    });
    paginationContainer.appendChild(nextButton);
}

function addToggleDescriptionListeners() {
    const toggleDescriptionButtons = document.querySelectorAll('.toggle-description');
    toggleDescriptionButtons.forEach(button => {
        button.addEventListener('click', (e) => {
            const description = e.target.previousElementSibling;
            description.classList.toggle('expanded');
            if (description.classList.contains('expanded')) {
                e.target.textContent = 'Згорнути';
            } else {
                e.target.textContent = 'Розгорнути';
            }
        });
    });
}

