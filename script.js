document.addEventListener('DOMContentLoaded', () => {
    // Відображаємо всі книжки при завантаженні сторінки
    displayBooks('all', 1); // 'all' - фільтр за замовчуванням, 1 - перша сторінка

    // Обробники подій для кнопок фільтрації (лише один раз!)
    filterButtons.forEach(button => {
        button.addEventListener('click', (e) => {
            filterButtons.forEach(btn => btn.classList.remove('active'));
            e.target.classList.add('active');
            currentFilter = e.target.dataset.filter;
            currentPage = 1; // Скидаємо сторінку при зміні фільтра
            displayBooks(currentFilter, currentPage);
        });
    });

    // Показати спливаюче вікно підписки через 5 секунд
    setTimeout(() => {
        showSubscribePopup();
    }, 5000);
});

// Функція для показу немодального вікна підписки
function showSubscribePopup() {
    if (localStorage.getItem('subscribed') === 'true') {
        return;
    }

    const subscribePopup = document.getElementById('subscribe-popup');
    subscribePopup.classList.add('show');

    const acceptButton = document.getElementById('accept-subscribe');
    const declineButton = document.getElementById('decline-subscribe');

    // Обробник для кнопки "Прийняти"
    acceptButton.addEventListener('click', () => {
        subscribePopup.classList.remove('show');
        localStorage.setItem('subscribed', 'true');
        showTemporaryMessage('Дякуємо за підписку!');
    }, { once: true });

    // Обробник для кнопки "Відхилити"
    declineButton.addEventListener('click', () => {
        subscribePopup.classList.remove('show');
        showTemporaryMessage('Можливо, іншим разом.');
    }, { once: true });
}

// Функція для показу короткочасного повідомлення
function showTemporaryMessage(message) {
    const messageDiv = document.createElement('div');
    messageDiv.classList.add('temporary-message');
    messageDiv.textContent = message;
    document.body.appendChild(messageDiv);

    // Стилі для тимчасового повідомлення
    messageDiv.style.cssText = `
        position: fixed;
        bottom: 20px;
        left: 50%;
        transform: translateX(-50%);
        background-color: #333;
        color: #fff;
        padding: 10px 20px;
        border-radius: 5px;
        z-index: 1001;
        opacity: 0;
        transition: opacity 0.5s ease-in-out;
    `;

    setTimeout(() => {
        messageDiv.style.opacity = '1';
    }, 50); // Невелика затримка для плавного показу

    setTimeout(() => {
        messageDiv.style.opacity = '0';
        messageDiv.addEventListener('transitionend', () => {
            messageDiv.remove();
        }, { once: true });
    }, 3000); // Повідомлення зникне через 3 секунди
}


const adModal = document.getElementById('ad-modal');
const closeAdModalButton = document.getElementById('close-ad-modal');
const adTimerDisplay = document.getElementById('ad-timer');
let adTimerInterval;
const adDisplayDuration = 5; // Секунд до активації кнопки

// Функція для показу модального вікна реклами
function showAdModal() {
    adModal.classList.add('show');
    closeAdModalButton.disabled = true;
    let timeLeft = adDisplayDuration;
    adTimerDisplay.textContent = timeLeft;

    adTimerInterval = setInterval(() => {
        timeLeft--;
        adTimerDisplay.textContent = timeLeft;
        if (timeLeft <= 0) {
            clearInterval(adTimerInterval);
            closeAdModalButton.disabled = false;
            closeAdModalButton.textContent = 'Закрити';
        }
    }, 1000);
}

// Функція для приховування модального вікна реклами
function hideAdModal() {
    adModal.classList.remove('show');
    clearInterval(adTimerInterval);
}

closeAdModalButton.addEventListener('click', hideAdModal);

let adShownByScroll = false;
window.addEventListener('scroll', () => {
    if (window.scrollY > 300 && !adShownByScroll) {
        showAdModal();
        adShownByScroll = true;
    }
});